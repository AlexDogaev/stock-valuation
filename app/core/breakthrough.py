"""Окна рывка — книга «Политэкономия росрынка», часть IV. ДВА ТИПА (Гл.15, расщепить обязательно):

1) ФРОНТИРНЫЙ рывок (Гл.12-14) — выход на мировой фронтир / экспорт высокого передела.
   Окно = созревание × чел.капитал × триггер (мультипликативно, все необходимы). НИЗКОЕ:
   рента ещё кормит, острый триггер off, чел.капитал доедается, + ловушка внешнего рынка
   (Запад закрыт, Китай самодостаточен). Зреет к 2035-45, но не «распахивается».

2) РЕНОВАЦИЯ = мобилизационно-инфраструктурный рывок (Гл.15-16-17) — пересборка ВЫБЫВАЮЩЕЙ
   советской материальной базы ТРИАДЫ Жильё-ЖКХ(коммунальные сети)-Электро(энергетика).
   Срок службы 50-60 лет → истекает синхронно в 2020-40-х. К этому Россия ПРИНУЖДЕНА (рынок —
   сама страна, внешний не нужен → ловушка внешнего рынка не мешает). НО реализация = ТРИАЖ
   (фискальные ножницы Гл.16: пик потребности совпал со сжатием ренты → чинят критическое и
   витринное, латают капремонтом, копят долг износа), реактивно-аварийно (Гл.17: «медведь
   просыпается на удар и дремлет под ржавчину»). Минору достаётся НЕ через операторов (генерация/
   сети/ЖКХ зажаты тарифом — ценопрессинг), а через ПОСТАВЩИКОВ освоенного оборудования замены:
   кабель, трубы, металлоконструкции, цемент — рыночная цена в детерминированный многолетний спрос.

⚠ Калиброванная гипотеза (как тектоника): направление важнее величины; Opus может уточнять факторы.
"""
from __future__ import annotations

from app.core.tectonic import period_for_year, PERIODS

# ── 1) ФРОНТИРНЫЙ рывок (Гл.14): мультипликативно, все факторы необходимы, окно низкое ──
FRONTIER = {
    "maturation":    {"P1": 0.30, "P2": 0.50, "P3": 0.70, "P4": 0.85},  # исчерпание ренты убирает «купить за нефть»
    "human_capital": {"P1": 0.70, "P2": 0.65, "P3": 0.55, "P4": 0.45},  # инж.школа сохранена, но доедается
    "trigger":       {"P1": 0.20, "P2": 0.30, "P3": 0.45, "P4": 0.55},  # медленный триггер крепнет; острый off
}
FRONTIER_LABELS = {"maturation": "созревание (исчерпание ренты)",
                   "human_capital": "человеческий капитал (инж.школа)",
                   "trigger": "триггер (медленный — исчерпание)"}

# ── 2) РЕНОВАЦИЯ Триады Жильё-ЖКХ-Электро (Гл.15-17): неизбежность ВЫСОКА, но триаж ──
# Неизбежность = износ × реактивный триггер (оба нарастают по мере выбытия базы). Фискальная
# способность (низкая, падает) — НЕ множитель неизбежности (не отменяет принуждение), а индикатор
# КАЧЕСТВА: чем ниже, тем больше «триаж» вместо «модернизационного бума».
RENOVATION = {
    "wear":     {"P1": 0.55, "P2": 0.70, "P3": 0.82, "P4": 0.85},  # износ: срок 50-60 лет истекает в 2020-40-х
    "reactive": {"P1": 0.50, "P2": 0.62, "P3": 0.75, "P4": 0.82},  # реактивный триггер: системные отказы нарастают
}
RENOVATION_LABELS = {"wear": "износ Триады (Жильё-ЖКХ-Электро, срок 50-60 лет)",
                     "reactive": "реактивный триггер (системные отказы — «медведь на удар»)"}
RENOVATION_FISCAL = {"P1": 0.45, "P2": 0.38, "P3": 0.32, "P4": 0.30}   # способность оплатить БЕЗ триажа (↓ ножницы)
RENOVATION_NODE = ("Узел для минора — ПОСТАВЩИКИ освоенного оборудования замены: кабель, трубы, "
                   "металлоконструкции, цемент (рыночная цена в детерминированный многолетний спрос). "
                   "НЕ операторы (генерация/сети/ЖКХ — зажаты тарифом, ценопрессинг).")


def _period(year: int, horizon: int) -> tuple[str, int]:
    target_year = year + max(0, int(horizon) - 1)         # к концу горизонта (год+H−1, чтобы H=1 → текущий)
    return period_for_year(target_year), target_year


def breakthrough_window(year: int, horizon: int = 1, overrides: dict | None = None) -> dict:
    """ФРОНТИРНОЕ окно (Гл.14) к концу горизонта. Окно ЗРЕЕТ во времени → на 20л зрелое (~P4)."""
    p, target_year = _period(year, horizon)
    f = {k: FRONTIER[k][p] for k in FRONTIER}
    if overrides:
        f.update({k: v for k, v in overrides.items() if k in FRONTIER and v is not None})
    prob = 1.0
    for k in FRONTIER:                                                   # мультипликативно — все необходимы
        prob *= f[k]
    pct = round(prob * 100, 1)
    level = "закрыто" if pct < 10 else ("зреет" if pct < 25 else "открывается")

    def _traj(pp):
        v = 1.0
        for k in FRONTIER:
            v *= FRONTIER[k][pp]
        return round(v * 100, 1)
    traj = {pp: _traj(pp) for pp in PERIODS}
    note = ("Окно ЗАКРЫТО: рента ещё кормит, острый триггер не включён — полный фронтир-рывок "
            "маловероятен. Зреет к 2035-45 (исчерпание ренты), но чел.капитал доедается + ловушка "
            "внешнего рынка → даже на пике не распахивается. Бенефициары минору ~недоступны."
            if level == "закрыто" else
            "Окно ЗРЕЕТ: исчерпание ренты крепит триггер. Но ловушка внешнего рынка (Запад закрыт, "
            "Китай самодостаточен) + доедание инж.школы держат фронтир-рывок в «возможно, но трудно».")
    return {"period": p, "target_year": target_year, "horizon": int(horizon),
            "prob_pct": pct, "level": level, "factors": f, "labels": FRONTIER_LABELS,
            "trajectory": traj, "note": note}


def renovation_window(year: int, horizon: int = 1, overrides: dict | None = None) -> dict:
    """РЕНОВАЦИЯ Триады Жильё-ЖКХ-Электро (Гл.15-17): неизбежность к концу горизонта.
    Высокая+нарастающая (принуждена), но реализация = триаж (фискальные ножницы), реактивно."""
    p, target_year = _period(year, horizon)
    f = {k: RENOVATION[k][p] for k in RENOVATION}
    if overrides:
        f.update({k: v for k, v in overrides.items() if k in RENOVATION and v is not None})
    prob = f["wear"] * f["reactive"]                                     # неизбежность форсированной волны
    pct = round(prob * 100, 1)
    level = "латентна" if pct < 30 else ("нарастает" if pct < 55 else "принуждена")
    fiscal = round(RENOVATION_FISCAL[p] * 100)
    quality = ("триаж (фискальные ножницы Гл.16: чинят критическое/витринное, латают капремонтом, "
               f"копят долг износа — фискальная способность ~{fiscal}%, не модернизационный бум)")

    traj = {pp: round(RENOVATION["wear"][pp] * RENOVATION["reactive"][pp] * 100, 1) for pp in PERIODS}
    note = (("ЛАТЕНТНА/НАРАСТАЕТ: советская база Триады (жильё/ЖКХ-сети/энергетика, срок 50-60 лет) "
             "копит износ; реновация идёт СЛЕДОМ за оборонзаказом (Гл.17), массовых отказов ещё нет. "
             if level != "принуждена" else
             "ПРИНУЖДЕНА: база Триады выбывает синхронно в 2030-40-х (отложенный платёж предъявлен весь "
             "сразу, Гл.16). Внешний рынок не нужен (рынок — сама страна) → ловушка фронтира не мешает. ")
            + quality.capitalize() + ". Реактивно-аварийно (Гл.17). " + RENOVATION_NODE)
    return {"period": p, "target_year": target_year, "horizon": int(horizon),
            "prob_pct": pct, "level": level, "factors": f, "labels": RENOVATION_LABELS,
            "fiscal_pct": fiscal, "quality": quality, "node": RENOVATION_NODE,
            "trajectory": traj, "note": note}
