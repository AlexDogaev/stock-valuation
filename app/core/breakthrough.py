"""Окно мобилизационного перехода (РЫВКА) — книга «Политэкономия росрынка», Гл.14.

Возможность ПОЛНОГО рывка (фронтир/экспорт высокого передела) = мультипликативная пороговая функция:
  созревание (исчерпание ренты убирает опцию «не развиваться, а купить за нефть»)
  × человеческий капитал (инж.школа: сохранность + воспроизводимость, доедание советского задела)
  × триггер (острый экзистенциальный — off; медленный — исчерпание ренты — крепнет).
Мультипликативность: низкий любой фактор → окно закрыто. Сейчас НИЗКОЕ (рента ещё кормит, острый
триггер не включён); зреет к 2035-40 по мере исчерпания ренты, но даже на пике не «распахивается»
(чел.капитал доедается, нет острого триггера) → рывок «возможно, но трудно и не гарантировано».

Инвест-смысл: пока окно закрыто, бенефициары ПРЕД-рывка (замещение известного — Элемент, Диасофт) имеют
ПОТОЛОК внутреннего рынка (Гл.13 ловушка внешнего рынка), а полный рывок минору в основном недоступен
(бенефициары непубличны/госкорпорации). Не оценивать пред-рывок как безграничный рост.

⚠ Калиброванная гипотеза (как тектоника): направление важнее величины; Opus может уточнять факторы.
"""
from __future__ import annotations

from app.core.tectonic import period_for_year, PERIODS

# pp-факторы по пятилеткам (P1 2026-30 … P4 2041-45), 0..1
FACTORS = {
    "maturation":    {"P1": 0.30, "P2": 0.50, "P3": 0.70, "P4": 0.85},  # исчерпание ренты убирает «купить за нефть»
    "human_capital": {"P1": 0.70, "P2": 0.65, "P3": 0.55, "P4": 0.45},  # инж.школа сохранена, но доедается
    "trigger":       {"P1": 0.20, "P2": 0.30, "P3": 0.45, "P4": 0.55},  # медленный триггер крепнет; острый off
}
LABELS = {"maturation": "созревание (исчерпание ренты)",
          "human_capital": "человеческий капитал (инж.школа)",
          "trigger": "триггер (медленный — исчерпание)"}


def breakthrough_window(year: int, overrides: dict | None = None) -> dict:
    """Окно рывка на текущую пятилетку + траектория. overrides (Opus/ручное) — доли 0..1 по факторам."""
    p = period_for_year(year)
    f = {k: FACTORS[k][p] for k in FACTORS}
    if overrides:
        f.update({k: v for k, v in overrides.items() if k in FACTORS and v is not None})
    prob = f["maturation"] * f["human_capital"] * f["trigger"]            # мультипликативно
    pct = round(prob * 100, 1)
    level = "закрыто" if pct < 10 else ("зреет" if pct < 25 else "открывается")
    traj = {pp: round(FACTORS["maturation"][pp] * FACTORS["human_capital"][pp] * FACTORS["trigger"][pp] * 100, 1)
            for pp in PERIODS}
    note = ("Окно ЗАКРЫТО: рента ещё кормит, острый триггер не включён — полный рывок маловероятен сейчас. "
            "Зреет к 2035-40 (исчерпание ренты), но чел.капитал доедается → даже на пике не распахивается. "
            "Пред-рывок (замещение) имеет ПОТОЛОК внутр. рынка; бенефициары полного рывка минору ~недоступны."
            if level == "закрыто" else
            "Окно ЗРЕЕТ: исчерпание ренты крепит триггер. Но ловушка внешнего рынка (Запад закрыт, Китай "
            "самодостаточен) + доедание инж.школы держат рывок в «возможно, но трудно». Пред-рывок ≠ рывок.")
    return {"period": p, "prob_pct": pct, "level": level, "factors": f, "labels": LABELS,
            "trajectory": traj, "note": note}
