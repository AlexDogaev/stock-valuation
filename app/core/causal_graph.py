"""Причинный граф цепочек (тектоническая рама §10) — НОВЫЙ модуль.

Граф причинно-следственных цепочек (тренд ур.0-1 → каскад → эмитент/сектор ур.4).
Edge НЕ в одиночной цепочке (отыграна), а в КОНВЕРГЕНЦИИ нескольких НЕЗАВИСИМЫХ путей
одного знака на одном узле — рынок видит фрагменты, не сумму на конкретном имени.

ТРИ ПРАВИЛА ДИСЦИПЛИНЫ (иначе граф = нефальсифицируемый нарративный шум):
1. ЗАТУХАНИЕ по длине: вес пути = ∏ весов рёбер (<1) → 2 звена сигнал, ≥3 почти шум.
2. EDGE В КОНВЕРГЕНЦИИ: сила узла = число НЕЗАВИСИМЫХ путей одного знака, не длина.
3. МЕХАНИЧЕСКАЯ НЕЗАВИСИМОСТЬ: одна причина, пересказанная трижды, ≠ три сигнала —
   дедуп по КОРНЕВОМУ механизму (атомизация и агломерация для девелопера = один механизм).

КРИТИЧНО (спек §10): модуль query-only, НЕ кормит живой сигнал, пока рёбра не провалидированы
(класс B, экспертный ввод). Иначе нарративный шум попадёт в фундамент. Строится последним.
"""
from __future__ import annotations

from dataclasses import dataclass


@dataclass(frozen=True)
class Edge:
    src: str          # узел-причина (тренд ур.0-1 или промежуточный)
    dst: str          # узел-следствие
    sign: int         # +1 попутный / −1 встречный
    weight: float     # P(ребра) × определённость тайминга ∈ (0,1]
    mechanism: str    # КОРНЕВОЙ механизм (для дедупа независимости, правило 3)


# Сид рёбер из примеров спека (§5, §9, §11.3). Класс B — экспертный ввод, флаги пересмотра.
EDGES: list[Edge] = [
    # детский ритейл: рождаемость − vs окрашенные пособия + (§11.3 — разные механизмы, СКЛАДЫВАТЬ)
    Edge("рождаемость↓", "детский_ритейл", -1, 0.8, "демография_рождаемость"),
    Edge("CBDC_окрашенные", "детский_ритейл", +1, 0.45, "госспрос_окрашенный"),
    Edge("премиум_единств_ребёнка", "детский_ритейл", +1, 0.5, "премиумизация"),
    # старение → медицина/фарма (один корневой механизм — НЕ дублировать как два)
    Edge("старение↑", "фарма", +1, 0.85, "демография_старение"),
    Edge("старение↑", "медицина", +1, 0.85, "демография_старение"),
    # недвижимость: атомизация + агломерация = ОДИН механизм (гор.жилищный спрос) — дедуп!
    Edge("атомизация↑", "девелопмент_агломерации", +1, 0.6, "гор_жилищный_спрос"),
    Edge("агломерация↑", "девелопмент_агломерации", +1, 0.6, "гор_жилищный_спрос"),
    # беспилотники → парковки↓ → свободная земля↑ → недвижимость (3 ребра — затухает).
    # Знаки на РЁБРАХ (не в именах узлов): (−1)·(−1)·(+1)=+1 — нетто положит. на девелопмент.
    Edge("беспилотники", "парковки", -1, 0.5, "автономный_транспорт"),
    Edge("парковки", "городская_земля", -1, 0.6, "автономный_транспорт"),
    Edge("городская_земля", "девелопмент_агломерации", +1, 0.6, "автономный_транспорт"),
    # автострахование: частота↓ (беспилотники) — прямое ребро (отыграно), для контраста
    Edge("беспилотники", "автострахование", -1, 0.5, "частота_аварий"),
    # EV → палладий − (автокатализатор)
    Edge("электромобили", "палладий", -1, 0.5, "автокатализатор_замещение"),
]

MAX_DEPTH = 4


@dataclass
class PathContribution:
    sign: int
    strength: float        # ∏ весов рёбер (затухание по длине, правило 1)
    mechanism: str         # корневой механизм (правило 3)
    chain: list[str]       # узлы пути от корня к цели


def _backward_paths(target: str, max_depth: int = MAX_DEPTH) -> list[PathContribution]:
    """Все входящие пути к target от корней (узлы без входящих рёбер). DFS назад."""
    out: list[PathContribution] = []

    def walk(node: str, sign: int, strength: float, mech: str | None, chain: list[str], depth: int) -> None:
        incoming = [e for e in EDGES if e.dst == node]
        if not incoming or depth >= max_depth:
            if mech is not None:            # дошли до корня (тренда)
                out.append(PathContribution(sign, strength, mech, list(reversed(chain + [node]))))
            return
        for e in incoming:
            # корневой механизм пути = механизм САМОГО ВЕРХНЕГО ребра (ближайшего к тренду)
            walk(e.src, sign * e.sign, strength * e.weight, e.mechanism, chain + [node], depth + 1)

    walk(target, 1, 1.0, None, [], 0)
    return out


def run_node(target: str) -> dict:
    """Прогон узла: дедуп по механизму (прав.3) → конвергенция независимых путей (прав.2)."""
    paths = _backward_paths(target)
    # правило 3: дедуп — сильнейший путь на (корневой механизм, знак)
    best: dict[tuple[str, int], PathContribution] = {}
    for p in paths:
        key = (p.mechanism, p.sign)
        if key not in best or p.strength > best[key].strength:
            best[key] = p
    contrib = sorted(best.values(), key=lambda c: -c.strength)
    pos = [c for c in contrib if c.sign > 0]
    neg = [c for c in contrib if c.sign < 0]
    net = round(sum(c.sign * c.strength for c in contrib), 3)
    convergence = max(len(pos), len(neg))     # правило 2: число независимых путей одного знака
    if convergence >= 2:
        verdict = f"конвергенция {convergence} независимых механизмов (одного знака) — СИГНАЛ"
    elif contrib:
        verdict = "одиночный путь — слабо (отыграно/шум), не основание для веса"
    else:
        verdict = "нет известных причинных путей в графе"
    return {
        "node": target, "net": net, "convergence": convergence,
        "verdict": verdict,
        "contributions": [
            {"sign": c.sign, "strength": round(c.strength, 3), "mechanism": c.mechanism,
             "chain": " → ".join(c.chain)} for c in contrib],
        "disclaimer": "Класс B (экспертные рёбра), query-only — НЕ входит в живой сигнал (§10).",
    }


def nodes() -> list[str]:
    """Все узлы-следствия (цели для прогона)."""
    return sorted({e.dst for e in EDGES})
