"""Индекс интеграции РФ в мир — мета-модулятор каналов трансмиссии (REVIEW D2, 20.06.2026).

ВЕКТОР, не скаляр (компоненты движутся несинхронно): 4 ИЗМЕРЕНИЯ × направление Запад/Восток.
Композит смазал бы разворот (торговля с Востоком растёт, пока финансовая с Западом падает).

КЛЮЧЕВАЯ СЦЕПКА (лечит изъян #3): ФИНАНСОВАЯ интеграция = переменный вход ТЕРМИНАЛЬНОГО P/E.
Хардкод P/E=6 = «вечный геодисконт»; интеграция растёт → дисконт снимается → P/E вверх; изоляция → вниз.
Двусторонне: высокая интеграция = и возможности, и уязвимость (финканал усиливает и приток-переоценку,
и заражение от S&P/risk-off). Все компоненты ЛАГИРОВАНЫ → структурный модулятор, НЕ тактический сигнал.
"""
from __future__ import annotations

from dataclasses import dataclass

# Текущая (июнь 2026) база изоляции: финансовая с Западом обрезана (нерезиденты ~3.5% оборота),
# торговля переориентирована на Восток. 0 = полная автаркия, 1 = полная интеграция.
DEFAULT_INTEGRATION = {
    "financial_west": 0.05, "financial_east": 0.25,
    "trade_west": 0.10, "trade_east": 0.70,
    "currency_west": 0.05, "currency_east": 0.55,   # доля расчётов в нетоксичных/юань
    "tech_west": 0.15, "tech_east": 0.50,           # параллельный импорт vs прямой
}

PE_BASELINE_FIN = 0.05      # текущая финансовая интеграция с Западом (точка отсчёта дисконта)
PE_SENS = 6.0              # чувствительность терминального P/E к финансовой деизоляции
PE_BOUNDS = (0.7, 3.0)     # множитель к базовому P/E (×0.7…×3.0)


@dataclass
class Integration:
    components: dict
    terminal_pe_mult: float        # множитель к базовому терминальному P/E
    riskoff_channel: float         # сила прямого S&P/risk-off канала (= фин.интеграция Запад)
    autarky_index: float           # сводный 0(интеграция)…1(автаркия), огрубление для картины
    note: str


def assess(components: dict | None = None, *, base_pe: float = 6.0) -> Integration:
    c = {**DEFAULT_INTEGRATION, **(components or {})}
    fin_west = c["financial_west"]
    # терминальный P/E: деизоляция (рост фин.интеграции с Западом) снимает геодисконт
    mult = 1.0 + PE_SENS * (fin_west - PE_BASELINE_FIN)
    mult = max(PE_BOUNDS[0], min(PE_BOUNDS[1], mult))
    # сводная автаркия = 1 − среднее интеграции (для общей картины, решения по компонентам)
    autarky = round(1.0 - sum(c.values()) / len(c), 3)
    note = (f"Фин.интеграция Запад {fin_west*100:.0f}% → терминальный P/E ×{mult:.2f}. "
            f"S&P/risk-off канал прямой слаб (через нефть); восстановится при возврате нерезидентов. "
            f"Автаркия {autarky:.2f} — структурный контекст, не тактика.")
    return Integration(components=c, terminal_pe_mult=round(mult, 3),
                       riskoff_channel=round(fin_west, 3), autarky_index=autarky, note=note)
