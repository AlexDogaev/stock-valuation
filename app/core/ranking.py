"""Скаляр ранжирования buy-and-hold = ГЕОМЕТРИЧЕСКОЕ среднее реальной доходности (REVIEW A5, 20.06.2026).

Для «купил-забыл» доходности ПЕРЕМНОЖАЮТСЯ → глубокий провал подрывает базу компаунда; арифметика
его недонаказывает. Геометрика = темп роста капитала = то, что нужно для buy-and-hold. Формализует
«прежде всего не разорись»: один глубокий хвост стирает годы компаунда. dom/EXPORT получает точную
цену = ГЛУБИНА ШОКОВОГО ХВОСТА (мелкий хвост экспортёра → меньше подрыв компаунда → выше геом).

ВИЛКА (A4) раскладывает «что может случиться» (база/шок/P); СКАЛЯР ранжирует «что предпочесть».
Не конкурируют: вилка — структура, геом-скаляр — порядок поверх неё.
"""
from __future__ import annotations

import math


def geom_real(scenarios: list[tuple[float, float]]) -> float:
    """Геометрическое ожидание: exp(Σ p·ln(1+r)) − 1. Сильнее наказывает глубокий хвост.

    scenarios: [(вероятность, реальная доходность за период), ...]. Глубокий минус (r≤−100%) → −1.
    """
    wsum = sum(p for p, _ in scenarios) or 1.0
    acc = 0.0
    for p, r in scenarios:
        acc += (p / wsum) * math.log(max(1e-6, 1.0 + r))
    return math.exp(acc) - 1.0


def fork(p_shock: float, base_real: float, shock_real: float) -> dict:
    """Вилка для buy-and-hold: база (шока нет) / шок (случился) / P(шок) + геом-скаляр ранжирования.

    Геом считается на ГОДОВОЙ реальной по двум ветвям (база p=1−p_shock, шок p=p_shock) —
    штрафует глубину шок-ветви (компаунд). Главное для РЕШЕНИЯ — вилка; геом — для РАНЖИРОВАНИЯ.
    """
    p = min(1.0, max(0.0, p_shock))
    g = geom_real([(1.0 - p, base_real), (p, shock_real)])
    return {"base_real": round(base_real, 4), "shock_real": round(shock_real, 4),
            "p_shock": round(p, 4), "geom_real": round(g, 4)}
