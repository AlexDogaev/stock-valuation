"""Тектоническая рама факторов (Инструкция_дев_тектоническая_рама, §1-7).

Пятиуровневая иерархия по скорости/отыгранности: ур.0 тектоника (демография, EROI) →
ур.1 институты → ур.2 макро → ур.3 настроение → ур.4 эмитент. Верхние уровни хуже отыграны
(рынок близорук <3-5 лет под высокую ставку дисконта) → edge = АРБИТРАЖ ГОРИЗОНТА.

Выход слоя: поправка к `g` эмитента из его СЕКТОРА × ТЕКУЩЕЙ ПЯТИЛЕТКИ, коридор −1.5…+3пп,
маршрутизация по DOMESTIC/EXPORTER (РФ-тектоника спроса — только для внутренних; экспортёр —
по глобальной демографии своего рынка, здесь не моделируется).

⚠ ЧИСЛА — КАЛИБРОВАННЫЕ ГИПОТЕЗЫ (§13): точные кривые строятся на однолетней возрастной
пирамиде Росстата. Пока — направление + пиковая пятилетка (форма верна, величины предварительны).
"""
from __future__ import annotations

from dataclasses import dataclass

# Пятилетки: P1 2026-30, P2 31-35, P3 36-40, P4 41-45
PERIODS = ("P1", "P2", "P3", "P4")
PERIOD_YEARS = {"P1": (2026, 2030), "P2": (2031, 2035), "P3": (2036, 2040), "P4": (2041, 2045)}

# §3 базовый g рынка — НЕМОНОТОННЫЙ (реальный): до сер.2030-х ~+1.5%, после ~+0.3%.
# Реального «прилива» нет → альфа в консолидаторах и структурных бенефициарах, не в индексе.
G_MARKET_BASE = {"P1": 0.015, "P2": 0.012, "P3": 0.006, "P4": 0.003}

# §6/§13 КАЛИБРОВКА на демпрогнозе Росстата-2046 (средний вариант). Источники-якоря:
#   • 60+: 35.9 млн (2023) → 37.3 млн (2046), доля 24.5%→26.9% — рост скромный (+3.9% за 22г),
#     УСКОРЯЕТСЯ к P3-P4 (когорты пика трудоспособного 2034-35 стареют в 60+);
#   • пик доли трудоспособного 2034-35 → дефицит раб.силы (издержки−) тектонически P3-P4
#     (острый дефицит СЕЙЧАС = конъюнктура СВО/эмиграция, НЕ тектоника — не зачитывать);
#   • дно рождаемости 2027 (~1.14 млн), спад 2023-27 → детское встречный сильнее всего в P1.
# Профили НЕ хардкодим — ВЫВОДИМ из факторных интенсивностей × демандных весов сектора.
# Магнитуды намеренно скромны (§7): рост 60+ мал в абсолюте, тектоника двигает g медленно.
# pp-профили факторов по пятилеткам (§7 NOTES_2, числовые колонки; грунт: демпрогноз-2046 +
# однолетняя пирамида Бюллетень-2024). Веса сектора (0-1) масштабируют их в поправку к g.
DEMO_FACTORS = {
    # СТАРЕНИЕ — РОВНОЕ плато (механизмы в противофазе: приток новых-60 пик P4 vs доживание 80+
    # пик P1-P3), брать ~скаляром. NOTES_3 §768: СКРОМНЕЕ (~+1.5-1.8, не +2.5) — РФ стареет «снизу»
    # (мало детей), а не «сверху»: ОПЖ стагнирует (особ. мужская) → питается ДОЛЕЙ пожилых, не долголетием.
    "aging":  {"P1": 1.5, "P2": 1.8, "P3": 1.8, "P4": 1.5},
    # РОЖДАЕМОСТЬ — ОСТРАЯ, дробить: дно P1 (эхо 1990-х), смягчение P3 (маткапитал-бум 2007-15 в
    # фертильном возрасте), снова вниз P4. Разворот P3 ХРУПОК (СКР↓ + культурная двойная нагрузка).
    "births": {"P1": -2.5, "P2": -1.5, "P3": -0.5, "P4": -2.0},
    # РАБ.СИЛА расщеплена по покрываемости ИИ (§7 колонка 3): один дефицит → противоположные знаки.
    "labor_phys": {"P1": -1.5, "P2": -2.0, "P3": -2.5, "P4": -2.5},  # физтруд (ИИ не покроет) дорожает → встречный
    "labor_cog":  {"P1": 0.3, "P2": 0.5, "P3": 0.7, "P4": 0.8},      # клерикал (ИИ вытесняет) → издержки↓ попутный (ур.1, скромно)
    "agglo":  {"P1": 0.6, "P2": 0.5, "P3": 0.3, "P4": 0.2},   # гор.спрос, резерв села исчерпывается
    "eroi":   {"P1": -0.3, "P2": -0.5, "P3": -0.8, "P4": -1.2},  # истощение/замещение, геология (P4)
}
# Демандные доли сектора по факторам (0-1) + _const = не-демо устойчивая сила.
SECTOR_FACTORS = {
    "Медицина":   {"aging": 1.0},                  # старение попутный сильный (плато ~+2-2.5пп)
    "Фарма":      {"aging": 1.0},
    # MDMG (под-сегмент, §7): старение+ vs родовспоможение− (дно P1, смягчение P3) + медики-руки−
    "Медицина_роды": {"aging": 1.0, "births": 0.4, "labor_phys": 0.2},
    # ритейл-консолидатор: барбелл доходов (премиум+дискаунтер, мультиформат X5 выигрывает),
    # агломерация+, физтруд логистики/магазинов дорожает−.
    "Ритейл":     {"agglo": 0.6, "labor_phys": 0.2, "_const": 0.4},
    "Банк":       {"aging": 0.2, "labor_cog": 0.6},  # сбережения пожилых+ ИИ режет клерикал (женский бэк-офис)+
    "Финансы":    {"aging": 0.2, "labor_cog": 0.5},
    "Телеком":    {"agglo": 0.2, "_const": 0.1},
    "Девелопмент": {"agglo": 0.6, "labor_phys": 0.3, "births": 0.2},  # стройка — острейший физдефицит
    "Электроэнергетика": {"_const": 0.1},
    "Детское":    {"births": 1.0, "_const": 0.3},   # _const = окрашенные пособия+ + премиум (§11.3)
    "Образование": {"births": 0.8, "_const": 0.2},
    "Нефтегаз":   {"eroi": 1.0},                    # EXPORTER → в спрос не идёт (см. routing)
}
# Под-сегмент ниже сектора (§7: вектор живёт под сектором — MDMG роды/прочее, не «Медицина» вообще)
ISSUER_SUBSECTOR = {"MDMG": "Медицина_роды"}


def _build_sector_tectonic() -> dict[str, dict[str, float]]:
    out: dict[str, dict[str, float]] = {}
    for sec, weights in SECTOR_FACTORS.items():
        prof = {}
        for p in PERIODS:
            v = weights.get("_const", 0.0)
            for f, fw in weights.items():
                if f != "_const":
                    v += fw * DEMO_FACTORS[f][p]
            prof[p] = round(v, 2)
        out[sec] = prof
    return out


SECTOR_TECTONIC = _build_sector_tectonic()

# ── ЭНЕРГОПЕРЕХОД / ИСЧЕРПАНИЕ РЕНТЫ (книга Гл.8/11) — ГЛОБАЛЬНЫЙ структурный сдвиг g по сырью. ──
# Применяется И К ЭКСПОРТЁРАМ (это демография их собственного — сырьевого — рынка, в отличие от
# РФ-демографии спроса). pp-профиль по пятилеткам: нефтегаз сжимается (пик нефти+исчерпание),
# золото/цветмет-электрификации растут («двойной выживальщик» / спрос энергоперехода).
ENERGY_TRANSITION = {
    "Нефтегаз":    {"P1": -0.5, "P2": -1.0, "P3": -1.5, "P4": -2.0},  # дойка на проедании (пик спроса на нефть)
    "Инфраструк.": {"P1": -0.3, "P2": -0.6, "P3": -0.9, "P4": -1.2},  # нефтетранспорт привязан к нефти (Транснефть)
    "Золото":      {"P1": 0.8, "P2": 0.9, "P3": 1.0, "P4": 1.0},      # двойной выживальщик: экспорт+девал-хедж+вне перехода
    "Удобрения":   {"P1": -0.3, "P2": -0.3, "P3": -0.3, "P4": -0.3},  # агро выживает, но враждебная форма (пошлины+ценопрессинг)
    "Энергетика":  {"P1": 0.3, "P2": 0.5, "P3": 0.7, "P4": 0.8},      # рост энергопотребления (электрификация); ценопрессинг — отдельно
}
ET_METAL_ELECTRIFICATION = {"GMKN", "RUAL", "ENPG"}   # медь/никель/алюминий — спрос мировой электрификации
ET_METAL_PROFILE = {"P1": 0.6, "P2": 0.9, "P3": 1.2, "P4": 1.5}       # растущий ветер энергоперехода
ET_METAL_NEUTRAL = {"ALRS": {"P1": -0.3, "P2": -0.3, "P3": -0.3, "P4": -0.3}}  # алмазы — luxury, не электрификация


def energy_transition_delta(sector: str | None, secid: str | None, period: str) -> float:
    """Сдвиг g от энергоперехода/исчерпания ренты (книга Гл.11). Доля. Применяется и к экспортёрам."""
    sid = (secid or "").upper()
    if sector == "Металл":
        if sid in ET_METAL_ELECTRIFICATION:
            return ET_METAL_PROFILE[period] / 100.0
        if sid in ET_METAL_NEUTRAL:
            return ET_METAL_NEUTRAL[sid][period] / 100.0
        return 0.0          # чёрная металлургия/сталь/титан — нейтрально (не электрификация)
    prof = ENERGY_TRANSITION.get(sector or "")
    return prof[period] / 100.0 if prof else 0.0


CORRIDOR_LO, CORRIDOR_HI = -0.015, 0.03   # §7 коридор множителя −1.5…+3пп (намеренно скромен)


@dataclass
class TectonicResult:
    period: str
    g_market_base: float       # базовый g рынка для текущей пятилетки (реальный)
    sector_delta: float        # поправка к g эмитента (доля, в коридоре), 0 для EXPORTER
    routed: bool               # применена ли РФ-тектоника (False для EXPORTER)
    peak_period: str | None    # пятилетка максимального попутного ветра сектора
    note: str


def period_for_year(year: int) -> str:
    for p, (a, b) in PERIOD_YEARS.items():
        if a <= year <= b:
            return p
    return "P4" if year > 2045 else "P1"


def _peak_period(profile: dict) -> str | None:
    return max(profile, key=profile.get) if profile else None


def tectonic_g(sector: str | None, currency_profile: str, *, year: int,
               secid: str | None = None) -> TectonicResult:
    """Тектоническая поправка к g эмитента: сектор (или под-сегмент) × пятилетка, маршрут по валюте."""
    period = period_for_year(year)
    gmb = G_MARKET_BASE.get(period, 0.01)
    # ЭНЕРГОПЕРЕХОД / исчерпание ренты (книга Гл.8/11) — глобальный сырьевой сдвиг, применяется И к экспортёрам.
    et = energy_transition_delta(sector, secid, period)
    et_txt = (f"; энергопереход {et*100:+.1f}пп (сырьевой сдвиг Гл.11)" if abs(et) >= 0.0005 else "")
    # §5.3/§7: РФ-тектоника СПРОСА — только DOMESTIC. EXPORTER — спрос по глобальному рынку (демографию не зачитываем),
    # НО сырьевой структурный сдвиг (энергопереход) — это и есть демография его рынка → учитываем.
    if currency_profile == "EXPORTER":
        delta = max(CORRIDOR_LO, min(CORRIDOR_HI, et))
        note = ("экспортёр: РФ-демография спроса не идёт" + (et_txt or " — сырьевой сдвиг нейтрален"))
        return TectonicResult(period, gmb, delta, abs(et) >= 0.0005, None, note)
    eff_sector = ISSUER_SUBSECTOR.get((secid or "").upper(), sector or "")  # под-сегмент ниже сектора
    profile = SECTOR_TECTONIC.get(eff_sector, {})
    raw = profile.get(period, 0.0) / 100.0 + et     # демография спроса + энергопереход
    delta = max(CORRIDOR_LO, min(CORRIDOR_HI, raw))
    if not profile and abs(et) < 0.0005:
        note = "сектор без тектонического профиля — нейтрально (вектор не детализирован)"
    else:
        pk = _peak_period(profile) if profile else None
        arrow = "попутный" if delta > 0 else ("встречный" if delta < 0 else "нейтральный")
        note = (f"тектоника {arrow} {delta*100:+.1f}пп ({period})"
                + (f"; пик ветра {pk}" if pk else "") + et_txt
                + " (оценка по сектору × пятилетке, число — калиброванная гипотеза)")
    return TectonicResult(period, gmb, delta, True, _peak_period(profile) if profile else None, note)
