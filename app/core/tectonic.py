"""Тектоническая рама факторов (Инструкция_дев_тектоническая_рама, §1-7).

Пятиуровневая иерархия по скорости/отыгранности: ур.0 тектоника (демография, EROI) →
ур.1 институты → ур.2 макро → ур.3 настроение → ур.4 эмитент. Верхние уровни хуже отыграны
(рынок близорук <3-5 лет под высокую ставку дисконта) → edge = АРБИТРАЖ ГОРИЗОНТА.

Выход слоя: поправка к `g` эмитента из его СЕКТОРА × ТЕКУЩЕЙ ПЯТИЛЕТКИ, коридор −1.5…+3пп,
маршрутизация по DOMESTIC/EXPORTER (РФ-тектоника спроса — только для внутренних; экспортёр —
по глобальной демографии своего рынка, здесь не моделируется).

⚠ ЧИСЛА — КАЛИБРОВАННЫЕ ГИПОТЕЗЫ (§13): точные кривые строятся на однолетней возрастной
пирамиде Росстата. Пока — направление + пиковая пятилетка (форма верна, величины предварительны).
"""
from __future__ import annotations

from dataclasses import dataclass

# Пятилетки: P1 2026-30, P2 31-35, P3 36-40, P4 41-45
PERIODS = ("P1", "P2", "P3", "P4")
PERIOD_YEARS = {"P1": (2026, 2030), "P2": (2031, 2035), "P3": (2036, 2040), "P4": (2041, 2045)}

# §3 базовый g рынка — НЕМОНОТОННЫЙ (реальный): до сер.2030-х ~+1.5%, после ~+0.3%.
# Реального «прилива» нет → альфа в консолидаторах и структурных бенефициарах, не в индексе.
G_MARKET_BASE = {"P1": 0.015, "P2": 0.012, "P3": 0.006, "P4": 0.003}

# §5/§6 секторный тектонический вектор по пятилеткам (пп к базовому g). Знак НЕОЧЕВИДЕН —
# сумма разнонаправленных сил, не «демография минус». Профиль строим только для НЕРАВНОМЕРНЫХ
# факторов (рождаемость/старение/EROI); монотонно-ровные — почти скаляр.
SECTOR_TECTONIC = {
    # старение P3-P4 (большие когорты за 60) — ВОСХОДЯЩИЙ профиль (кейс MDMG §11.1)
    "Медицина":   {"P1": 0.5, "P2": 1.5, "P3": 2.8, "P4": 2.5},
    "Фарма":      {"P1": 0.8, "P2": 1.6, "P3": 2.6, "P4": 2.4},
    # консолидатор продуктового ритейла: атомизация+ агломерация+ консолидация+, депопуляция− по объёму
    "Ритейл":     {"P1": 0.8, "P2": 1.0, "P3": 0.8, "P4": 0.5},
    # банки: кредитование[раб.сила−] → управление сбережениями пожилых+ (растёт к P3-P4)
    "Банк":       {"P1": 0.0, "P2": 0.3, "P3": 0.6, "P4": 0.6},
    "Финансы":    {"P1": 0.0, "P2": 0.3, "P3": 0.5, "P4": 0.5},
    # телеком/ЦОД: enabler+ атомизация+, насыщен → слабо ровный
    "Телеком":    {"P1": 0.2, "P2": 0.3, "P3": 0.3, "P4": 0.2},
    # девелоперы: атомизация++/агломерация+ vs депопуляция−−/старение− → нетто слабо, гео-расщеп
    "Девелопмент": {"P1": 0.2, "P2": 0.2, "P3": 0.0, "P4": -0.2},
    # электроэнергетика: нейтрально (апсайд ЦОД-под-ИИ — ур.1, не сюда)
    "Электроэнергетика": {"P1": 0.0, "P2": 0.1, "P3": 0.2, "P4": 0.2},
    # детское/образование: рождаемость−−− (дно P1, разворот к P3), смягчено окрашенными пособиями (§11.3)
    "Детское":    {"P1": -1.0, "P2": -0.6, "P3": -0.2, "P4": 0.0},
    "Образование": {"P1": -0.8, "P2": -0.5, "P3": -0.2, "P4": 0.0},
    # нефтегаз: EROI− встречный, пик P4 (+ замещение ур.1) — но EXPORTER → в спрос не идёт (см. routing)
    "Нефтегаз":   {"P1": -0.3, "P2": -0.5, "P3": -0.8, "P4": -1.2},
}

CORRIDOR_LO, CORRIDOR_HI = -0.015, 0.03   # §7 коридор множителя −1.5…+3пп (намеренно скромен)


@dataclass
class TectonicResult:
    period: str
    g_market_base: float       # базовый g рынка для текущей пятилетки (реальный)
    sector_delta: float        # поправка к g эмитента (доля, в коридоре), 0 для EXPORTER
    routed: bool               # применена ли РФ-тектоника (False для EXPORTER)
    peak_period: str | None    # пятилетка максимального попутного ветра сектора
    note: str


def period_for_year(year: int) -> str:
    for p, (a, b) in PERIOD_YEARS.items():
        if a <= year <= b:
            return p
    return "P4" if year > 2045 else "P1"


def _peak_period(profile: dict) -> str | None:
    return max(profile, key=profile.get) if profile else None


def tectonic_g(sector: str | None, currency_profile: str, *, year: int) -> TectonicResult:
    """Тектоническая поправка к g эмитента: сектор × текущая пятилетка, маршрут по валюте."""
    period = period_for_year(year)
    gmb = G_MARKET_BASE.get(period, 0.01)
    # §5.3/§7: РФ-тектоника спроса — только DOMESTIC. EXPORTER — по глобальному рынку (не здесь).
    if currency_profile == "EXPORTER":
        return TectonicResult(period, gmb, 0.0, False, None,
                              "экспортёр: спрос по ГЛОБАЛЬНОЙ тектонике своего рынка, РФ-демография в выручку не идёт")
    profile = SECTOR_TECTONIC.get(sector or "", {})
    raw = profile.get(period, 0.0) / 100.0          # пп → доля
    delta = max(CORRIDOR_LO, min(CORRIDOR_HI, raw))
    if not profile:
        note = "сектор без тектонического профиля — нейтрально (вектор не детализирован)"
    else:
        pk = _peak_period(profile)
        arrow = "попутный" if delta > 0 else ("встречный" if delta < 0 else "нейтральный")
        note = (f"тектоника {arrow} {delta*100:+.1f}пп ({period}); пик ветра {pk} "
                f"(оценка по сектору × пятилетке, число — калиброванная гипотеза)")
    return TectonicResult(period, gmb, delta, True, _peak_period(profile), note)
